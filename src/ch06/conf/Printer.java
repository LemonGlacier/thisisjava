package ch06.conf;

public class Printer {
	public static void println(int v) {
		System.out.println(v);
	}
	public static void println(boolean v) {
		System.out.println(v);
	}
	public static void println(double v) {
		System.out.println(v);
	}
	public static void println(String v) {
		System.out.println(v);
	}
}
